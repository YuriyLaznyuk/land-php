                <p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8 Упаковка, маркировка, транспортирование и
                    хранение<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
                <p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.1 Прутки диаметром 35 мм и менее связывают в пучки,
                    состоящие не менее чем из трех прутков. Масса пучка должна быть не более 80 кг. Пучок должен быть
                    перевязан в два оборота проволокой диаметром не менее 1,2 мм по ГОСТ 3282 не менее чем в двух
                    местах, а при длине прутков свыше 3 м - не менее чем в трех местах таким образом, чтобы исключить
                    взаимное перемещение прутков в пучке. Концы проволоки соединяют скруткой не менее пяти витков.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Тянутые
                    прутки диаметром 18 мм и менее допускается связывать в бухты массой не более 140 кг. Каждая бухта
                    должна быть перевязана проволокой по ГОСТ 3282 не менее чем в трех местах равномерно по окружности
                    бухты.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Допускается связывать
                    прутки любого диаметра в пучки массой до 500 кг. Каждый пучок должен быть перевязан проволокой в два
                    оборота диаметром не менее 3 мм по ГОСТ 3282 или лентой размером не менее 0,3х30 мм по ГОСТ 3560 не
                    менее чем в трех местах равномерно по длине пучка таким образом, чтобы исключалось взаимное
                    перемещение прутков в пучке.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;По
                    требованию потребителя тянутые прутки диаметром до 10 мм включительно высокой и повышенной точности
                    изготовления упаковывают в один или два слоя в синтетические или нетканые материалы, обеспечивающие
                    сохранность качества прутков, перевязывают их поверх упаковочного материала проволокой любого
                    диаметра, обеспечивающей необходимую прочность увязки по ГОСТ 3282 не менее чем в двух местах.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Укрупнение
                    грузовых мест в транспортные пакеты проводят по нормативным документам. Габаритные размеры пакетов -
                    по ГОСТ 24597.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Масса
                    транспортного пакета при транспортировании в крытых вагонах не должна превышать 1250 кг.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Допускается
                    транспортировать прутки в пакетах массой до 1500 кг без увязки в пучки при отсутствии перегрузки в
                    пути.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.2 Пакетирование пучков,
                    бухт и отдельных прутков, не связанных в пучки, проводят на поддонах по ГОСТ 9557 или без поддонов с
                    использованием брусков высотой не менее 50 мм с обвязкой проволокой диаметром не менее 2 мм в два
                    оборота по ГОСТ 3282 или лентой размером не менее 0,3х30 мм по ГОСТ 3560, или с использованием
                    пакетируемых строп из проволоки по ГОСТ 3282 диаметром не менее 5 мм со скруткой не менее трех
                    витков. Концы проволоки соединяют скруткой не менее пяти витков, ленты - в замок.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Упаковка
                    продукции, отправляемой в районы Крайнего Севера и приравненные к ним местности, - по ГОСТ
                    15846.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Могут применяться также
                    и другие виды упаковки и упаковочных материалов по нормативным документам, по прочности не
                    уступающие указанным в стандарте и обеспечивающие сохранность качества продукции.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;По
                    согласованию изготовителя с потребителем может быть использована многооборотная тара.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Требования
                    в отношении особых видов упаковки следует согласовать при заказе.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.3
                    К каждому пучку (бухте) прикрепляют ярлык (металлический, деревянный, фанерный, пластиковый),
                    обеспечивающий сохранность на нем следующих данных:<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                    товарного знака или наименования и товарного знака предприятия-изготовителя;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                    наименования страны-изготовителя;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                    условного обозначения прутков;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                    номера партии;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- штампа
                    технического контроля или номера контролера.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.4
                    К каждому прутку диаметром более 35 мм, который поставляется без связки в пучок, должен быть
                    прикреплен ярлык или выбиты данные по 8.3.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.5
                    Если тянутые прутки предназначаются для обработки на автоматах, на ярлыках и в документах о качестве
                    после размера дополнительно указывают буквы АВ.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.6
                    Транспортирование прутков длиной до 3 м проводят в крытых транспортных средствах всех видов в
                    соответствии с правилами перевозки грузов, действующими на транспорте данного вида.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Транспортные
                    средства для прутков длиной более 3 м определяют в соответствии с правилами перевозки грузов,
                    действующими на транспорте данного вида.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.7
                    Прутки должны храниться в крытых складских помещениях в условиях, исключающих механические
                    повреждения, воздействие влаги и активных химических
                    веществ.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;При соблюдении
                    указанных условий хранения потребительские свойства прутков не меняются.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
