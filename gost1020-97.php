            <p>4.3 Масса каждой чушки не должна превышать 42 кг. Для кремнистых латуней масса чушек допускается до 45 кг. Форму и размеры чушек устанавливает изготовитель.</p>
            <p>4.4 На поверхности чушек не должно быть наплывов, заливов, вздутостей, шлаковых и ино­родных включений.</p>
            <p>Общая площадь поверхности, занимаемой окисньми пленками и пленами, не должна превы­шать 15% поверхности чушек.</p>
            <p>4.5 В изломе чушки должны быть плотными и без инородных включений.</p>
            <p>4.6 Дополнительные технические требования могут быть установлены в договорном соглаше­нии потребителя с изготовителем.</p>
            <p>4.7 На каждой чушке должны быть нанесены:<br /> -товарный знак или товарный знак и наименование предприятия-изготовителя;<br /> -номер плавки;<br /> -цветная маркировка сплава.</p>
            <p>4.8 Цветную маркировку наносят на торцах чушек несмываемой краской в виде вертикальных полос и крестов для латуни марки:<br /> - ЛС.............. белой;<br /> - ЛСч.............белой и белого креста;<br /> - ЛСд.............двух белых;<br /> - ЛСдч...........двух белых и белого креста;<br /> - ЛОС............белой, черной и белой;<br /> - ЛК...............белой и красной;<br /> - ЛК1.............белой, красной и белой;<br /> - ЛК2.............белой и двух красных;<br /> - ЛКС.............черной, красной и черной;<br /> - ЛМцС...........белой и синей;<br /> - ЛМцЖ..........белой и зеленой;<br /> - ЛА................белой и желтой;<br /> - ЛМцКА.........трех красных;<br /> - ЛАЖМц........белой и коричневой;<br /> - ЛМцСК.........красной;<br /> - ЛМцСКА.......двух красных.</p>
            <p align="center"><b>5. Правила приемки</b></p>
            <p>5.1 Сплавы предъявляют к приемке партиями. Партия должна содержать чушки из сплава одной марки, одной или нескольких плавок и сопровождаться одним документом о качестве, содержащим:<br /> -товарный знак или товарный знак и наименование предприятия-изготовителя;<br /> -марку сплава;<br /> -номер партии;<br /> -массу партии;<br /> -номер(а) плавки (плавок);<br /> -результат(ы) химического анализа плавки (плавок);<br /> -дату изготовления;<br /> -обозначение настоящего стандарта.</p>
            <p>5.2 Партия не должна содержать более 1 % ломаных чушек. В партии, предназначенной для экспорта, ломаные чушки не допускаются.</p>
            <p>5.3 Проверке качества поверхности подвергают не менее 20 чушек.</p>
            <p>5.4 Контроль качества излома проводят по требованию потребителя. Для контроля качества излома отбирают не менее двух чушек.</p>
            <p>5.5 Для контроля химического состава от каждой плавки отбирают не менее двух чушек. На предприятии-изготовителе допускается отбирать пробы от жидкого металла по методике, утвержденной в установленном порядке.</p>
            <p>5.6 При получении неудовлетворительных результатов испытаний хотя бы по одному из показателей по нему проводят повторные испытания на удвоенном количестве образцов, отобранных от той же плавки. Результаты повторного испытания распространяют на всю партию.</p>
            <p align="center"><b>6. Методы испытаний</b></p>
            <p>6.1 Для проверки качества излома отобранные чушки наполовину надпиливают и ломают.</p>
            <p>6.2 Проверку качества поверхности и излома проводят визуально.</p>
            <p>6.3 Отбор и подготовку проб для химического анализа проводят по ГОСТ 24231, для спектрального анализа — по методике, утвержденной в установленном порядке.</p>
            <p>При отборе и подготовке проб для химического анализа должны соблюдаться требования по безопасному ведению работ в соответствии с ГОСТ 12.1.005, ГОСТ 12.1.007, ГОСТ 12.4.013, ГОСТ 12.4.021 и правилами, утвержденными в установленном порядке.</p>
            <p>6.4 Определение химического состава проводят по ГОСТ 25086, ГОСТ 1652.1 — ГОСТ 1652.13.</p>
            <p>Допускается определять химический состав другими методами, не уступающими по точности стандартизованным.</p>
            <p>При возникновении разногласий в оценке химического состава его определение проводят по ГОСТ 1652.1 - ГОСТ 1652.13.</p>
            <p>Содержание фосфора и никеля определяют по требованию потребителя.</p>
            <p align="center"><b>7. Транспортирование и хранение</b></p>
            <p>7.1 Чушки транспортируют в пакетах по ГОСТ 21399, ГОСТ 26653. Пакеты должны состоять из чушек одной марки.</p>
            <p>Пакеты скрепляют упаковочной лентой по ГОСТ 3560 или стальной проволокой диаметром не менее 10 мм по ГОСТ 3282.</p>
            <p>Допускается применять другие средства скрепления по ГОСТ 21650, обеспечивающие сохранность пакета.</p>
            <p>Транспортная маркировка пакетов — по ГОСТ 14192. На боковой стороне пакета крепят металлический или деревянный ярлык материалами, обеспечивающими его сохранность.</p>
            <p>7.2 Пакеты транспортируют всеми видами транспорта в соответствии с правилами перевозки грузов, действующими на данном виде транспорта.</p>
            <p>Пакеты, предназначенные для экспорта, транспортируют в крытых транспортных средствах.</p>
            <p>7.3 Чушки хранят в условиях, обеспечивающих сохранность их качества.</p>
