            <p align="center"><b>3. Определения</b></p>
            <p>В настоящем стандарте применяют следующие термины с соответствующими определениями:</p>
            <p>3.1 шлаковые включения: Дефект в виде полости, заполненной шлаком.</p>
            <p>3.2 инородные включения: Дефект в виде металлического или неметаллического включения, имеющего поверхность раздела с основным металлом.</p>
            <p>3.3 плена: Дефект в виде металлического или окисного слоя на поверхности отливки, возникающий при недостаточно спокойной заливке металла.</p>
            <p>3.4 наплыв: Дефект в виде натекания металла на поверхность основного металла.</p>
            <p>3.5 залив: Дефект в виде металлического выступа на чушке, возникающий вследствие попадания жидкого металла в зазоры по разъемам изложницы (формы).</p>
            <p>3.6 вздутость: Дефект в виде вспучивания поверхности металла, возникающий на чушках из-за присутствия газовых пузырей или неметаллических включений.</p>
