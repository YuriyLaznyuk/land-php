        <p align="center"><b>Сплавы медно-цинковые (латуни), литейные. Марки. Cast copper-zinc alloys brass. Grades.</b></p>
        <p align="center"><b>ГОСТ 17711-93</b></p>
        <p align="center"><b>ОКП&nbsp; 41 1330</b></p>
        <p><b>&nbsp;Дата введения 01.01.95</b></p>
        <p>Настоящий стандарт устанавливает марки медно-цинковых сплавов (латуней) в отливках.</p>
        <p>1. Марки и химический состав медно-цинковых сплавов должны соответствовать требованиям, приведенным в табл. 1; механические свойства — приведенным в табл. 2.</p>
        <p>2. Определение химического состава сплавов проводят по ГОСТ 1652.1 — ГОСТ 1652.13. Допускается определять химический состав сплавов другими методами, обеспечивающими точность не ниже приведенных в указанных стандартах. При возникновении разногласий в оценке химического состава определение проводят по ГОСТ 1652.1 — ГОСТ 1652.13.</p>
        <p>3. Механические свойства сплавов определяют на отдельно отлитых образцах или образцах, изготовленных из отдельно отлитых проб. При заливке пробы необходимо обеспечить ее питание по всей длине. Диаметр рабочей части образцов 10 мм, расчетная длина — 50 мм. Количество образцов и проб указывается в нормативно-технической документации на конкретную продукцию.</p>
        <p>4. Способ литья образцов и проб должен соответствовать способу изготовления отливок.</p>
        <p>Для определения механических свойств при литье под давлением или центробежным способом допускается заливать пробы в кокиль.</p>
        <p>5. Испытание образцов на растяжение проводят по ГОСТ 1497.</p>
        <p>6. Определение твердости по Бринеллю проводят по ГОСТ 9012.</p>
