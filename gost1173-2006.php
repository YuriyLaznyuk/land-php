                Рулоны фольги и лент толщиной до 0,50 мм включительно должны быть обернуты бумагой, упакованы в
                деревянные ящики и переложены картоном.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Рулоны
                лент толщиной более 0,5 мм должны быть обвязаны лентой размером не менее 0,2х20 мм и обернуты в
                синтетический или нетканый материал, обеспечивающий сохранность качества продукции.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.3
                Ленты, разрезанные на длины, и холоднокатаные листы толщиной до 2,00 мм должны быть упакованы в
                деревянные ящики.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Допускается
                ленты, разрезанные на длины, и холоднокатаные листы толщиной до 2,00 мм включительно упаковывать в
                пачки. Пачки должны быть обернуты бумагой и защищены сверху и снизу досками или деревянными щитами,
                размеры которых соответствуют ширине и длине пачки, и перевязаны не менее чем в двух местах или
                крестообразно лентой размером не менее 0,3х20 мм или проволокой толщиной не менее 2 мм. Скрепление
                концов: проволоки - скруткой не менее трех витков, ленты - в
                замок.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ленты, разрезанные на длины,
                холоднокатаные листы толщиной более 2,00 мм и горячекатаные листы должны быть упакованы в пачки с
                односторонней защитой щитами по размерам пачки, как указано
                выше.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Листы длиной 4 м и более
                должны быть согнуты до соприкосновения кромок.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.4
                Плиты поставляют на деревянных обрешетках. Сверху плиту закрывают картоном. Плиту перевязывают не менее
                чем в двух местах в поперечном и продольном направлениях лентой размером не менее 0,8х20 мм.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.5
                В качестве упаковочных средств и материалов и средств скрепления следует применять:<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                бумагу по ГОСТ 8273;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                гофрированный картон по ГОСТ 7376;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                картон по ГОСТ 7933;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- деревянные
                ящики типов I, II, III по ГОСТ 2991 и ГОСТ 10198. <br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Размеры
                ящиков - по ГОСТ 21140 или техническим документам;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                доски и щиты по техническим документам;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                синтетические и нетканые материалы по техническим документам;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                ленту по ГОСТ 3560;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- проволоку по
                ГОСТ 3282.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Допускается применять
                другие виды упаковки и упаковочных материалов, не уступающие по прочности перечисленным выше и
                обеспечивающие сохранность качества продукции.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.6
                К каждому рулону или пачке или на каждую плиту должен быть прикреплен ярлык с указанием:<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                товарного знака или наименования и товарного знака предприятия-изготовителя;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                наименования страны-изготовителя;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                условного обозначения фольги, лент, листов и плит;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                номера партии;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- штампа
                технического контроля.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.7 Без
                упаковки в ящики, бумагу, синтетические и нетканые материалы допускается транспортировать рулоны и пачки
                в контейнерах по ГОСТ 15102, ГОСТ 22225, ГОСТ 20435 и ГОСТ 18477 в один адрес без перегрузки в пути. В
                этом случае между рядами рулонов и пачек должен быть проложен гофрированный картон по ГОСТ 7376.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;При
                транспортировании в контейнерах рулоны и пачки должны быть уложены и укреплены таким образом, чтобы
                исключалась возможность их перемещения в контейнере. Кроме того, рулоны и пачки должны быть защищены от
                коррозии, загрязнений и механических повреждений.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.8
                В каждый ящик или контейнер должен быть вложен упаковочный лист, содержащий:<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                товарный знак или наименование и товарный знак предприятия-изготовителя;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                наименование страны-изготовителя;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                условное обозначение фольги, лент, листов и плит;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                номер партии;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- массу нетто;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-
                массу брутто;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- номер
                упаковщика.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.9 Транспортная
                маркировка - по ГОСТ 14192 с нанесением манипуляционного знака "Беречь от влаги".<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.10
                Укрупнение грузовых мест в транспортные пакеты проводят в соответствии с требованиями ГОСТ 26663.
                Средства скрепления в транспортные пакеты - по ГОСТ 21650.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Пакетирование
                проводят на поддонах по ГОСТ 9557 или ГОСТ 9078 или без поддонов с использованием брусков высотой не
                менее 50 мм с обвязкой в продольном и поперечном направлениях проволокой диаметром не менее 2 мм по ГОСТ
                3282 или лентой размерами не менее 0,3х20 мм по ГОСТ 3560. Скрепление концов: проволоки - скруткой не
                менее пяти витков, ленты - в замок.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Масса
                грузового места не должна превышать 2000 кг. Масса грузового места в крытых вагонах не должна превышать
                1250 кг.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.11 Упаковывание фольги,
                лент, листов и плит, отправляемых в районы Крайнего Севера и приравненные к ним местности, - по ГОСТ
                15846 по группе "Металлы и металлические изделия".<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.12
                Фольга, ленты, листы и плиты транспортируют всеми видами транспорта в крытых транспортных средствах в
                соответствии с правилами перевозки грузов, действующими на транспорте данного вида.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;8.13
                Фольга, ленты, листы и плиты должны храниться в крытых помещениях. При хранении и транспортировании
                фольга, ленты, листы и плиты должны быть защищены от механических повреждений, воздействия влаги и
                активных химических веществ.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;У
                потребителя фольга, ленты, листы и плиты должны быть выдержаны на складе в упаковке изготовителя не
                менее двух суток для выравнивания температуры фольги, лент, листов и плит с температурой помещения. По
                истечении указанного срока фольга, ленты, листы и плиты должны быть распакованы.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;При
                соблюдении указанных условий хранения потребительские свойства медных фольги, лент, листов и плит при
                хранении не изменяются.<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
