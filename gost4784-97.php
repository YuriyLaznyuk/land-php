        <h1>3. Общие требования</h1>
        <p>Марки и химический состав алюминия должны соответствовать указанным в таблице 1.</p>
        <p>3.1. Соотношение железа и кремния в алюминии должно быть не менее единицы.</p>
        <p>3.2. Марки и химический состав алюминиевых сплавов систем алюминий-медь-магний и алюминий-медь-марганец должны соответствовать указанным в таблице 2.</p>
        <p>3.3. Марки и химический состав алюминиевых сплавов системы алюминий-марганец должны соответствовать указанным в таблице 3.</p>
        <p>3.3.1. Соотношение железа и кремния в сплаве АМцС должно быть больше единицы.</p>
        <p>3.4. Марки и химический состав алюминиевых сплавов системы алюминий-магний должны соответствовать указанным в таблице 4.</p>
        <p>3.4.1. В сплаве марки АМг 2, предназначенном для изготовления ленты, применяемой в качестве тары-упаковки в пищевой промышленности, массовая доля магния должна быть от 1,8 до 3,2 %.</p>
        <p>3.5. Марки и химический состав алюминиевых сплавов системы алюминий-магний-кремний должны соответствовать указанным в таблице 5.</p>
        <p>3.6. Марки и химический состав алюминиевых сплавов системы алюминий-цинк-магний должны соответствовать указанным в таблице 6.</p>
        <p>3.7. В алюминии и алюминиевых сплавах, указанных в таблицах 1- 6, допускается частичная или полная замена титана бором или другими модифицирующими добавками, обеспечивающими мелкозернистую структуру.</p>
        <p>3.8. В алюминии и алюминиевых сплавах, полуфабрикаты из которых применяют при изготовлении изделий пищевого назначения, массовая доля свинца должна быть не более 0,15 %, массовая доля мышьяка - не более 0,015 %. Марки алюминия и алюминиевых сплавов пищевого назначения дополнительно маркируются буквой «Ш».</p>
        <p><b> (Измененная редакция.</b><b> Изм . № 1 ).</b></p>
        <p>3.9. Химический состав сплавов марок Д 1, Д16, АМг5 и В95, предназначенных для изготовления проволоки для холодной высадки, должен соответствовать указанному в таблице 7. При этом марка дополнительно маркируется буквой "П".</p>
        <p>3.10. Марки и химический состав алюминия и алюминиевых сплавов, предназначенных для изготовления сварочной проволоки, должны соответствовать указанным в таблице 8.</p>
        <p>3.11. Содержание элементов в таблицах 1- 8 максимальное, если не указаны пределы.</p>
        <p>3.12. Химический состав алюминия и алюминиевых сплавов в таблицах 1- 8 дан в процентах по массе. Расчетное значение или значение, полученное из анализа, округляют в соответствии с правилами округления, приведенными в приложении А.</p>
        <p>3.13. В графу "Прочие элементы" входят элементы, содержание которых не представлено, а также элементы, не указанные в таблицах.</p>
        <p>3.14. В расчет прочих элементов включают массовые доли элементов, выраженные с точностью до второго десятичного знака и равные 0,01 % и более.</p>
        <p>3.15. Массовая доля бериллия устанавливается по расчету шихты, не определяется, а обеспечивается технологией производства.</p>
        <p>3.16. В протоколах анализа химического состава дается обобщенное заключение по соответствию содержания прочих элементов требованиям <span class="alink">ГОСТ 4784</span>, исходя их единичных значений и суммы значений этих элементов.</p>
        <p>Содержание каждого из прочих элементов в протоколах не указывают.</p>
