        <h1 align="center">ПРИЛОЖЕНИЕ А <br /> (справочное)</h1>
        <p align="center"><b> Правила округления</b></p>
        <p>А.1. Округление представляет собой отбрасывание значащих ци фр справа до определенного разряда с возможным изменением цифры этого разряда.</p>
        <p>Пример: Округление числа 132,48 до четырех значащих цифр будет 132,5.</p>
        <p>А.2. В случае , если первая из отбрасываемых цифр (считая слева направо) меньше 5, последняя сохраняемая цифра не меняется.</p>
        <p>Пример: Округление числа 12,23 до трех значащих цифр дает 12,2.</p>
        <p>А.3. В случае , если первая из отбрасываемых цифр (считая слева направо) равна 5, последняя сохраняемая цифра увеличивается на единицу.</p>
        <p>Пример: Округление числа 0,145 до двух значащих цифр дает 0,15.</p>
        <p><b>Примечание</b> - В случаях, когда следует учитывать результаты предыдущих округлений, поступают следующим образом:</p>
        <p>- если отбрасываемая цифра получилась в результате предыдущего округления в большую сторону, то последняя сохраняемая цифра сохраняется.</p>
        <p>Пример: Округление до одной значащей цифры числа 0,15 (полученного после округления числа 0,149) дает 0,1;</p>
        <p>- если отбрасываемая цифра получилась в результате предыдущего округления в меньшую сторону, то последняя оставшаяся цифра увеличивается на единицу (с переходом при необходимости в следующие разряды).</p>
        <p>Пример: Округление числа 0,25 (полученного в результате предыдущего округления числа 0,25) дает 0,3.</p>
        <p>А.4. В случае , если первая из отбрасываемых цифр (считая слева направо) больше 5, то последняя сохраняемая цифра увеличивается на единицу.</p>
        <p>Пример: Округление числа 0,156 до двух значащих цифр дает 0,16.</p>
        <p>А.5. Округление следует выполнять сразу до желаемого количества значащих цифр, а не по этапам.</p>
        <p>Пример: Округление числа 565,46 до трех значащих цифр производится непосредственно на 565.</p>
        <p>Округление по этапам привело бы:</p>
        <p>на I этапе к 565, 5;</p>
        <p>на II этапе к 566 (ошибочно).</p>
        <p>А.6. Целые числа округляют по тем же правилам, как и дробные.</p>
        <p>Пример: Округление числа 12456 до двух значащих цифр дает 12 × 10<sup>3</sup>.</p>
