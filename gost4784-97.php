        <h1>1. Область применения</h1>
        <p>Настоящий стандарт распространяется на алюминий и деформируемые алюминиевые сплавы, предназначенные для изготовления полуфабрикатов (лент в рулонах, листов, кругов-дисков, плит, полос, прутков, профилей, шин, труб, проволоки, поковок и штампованных поковок) методом горячей или холодной деформации, а также слябов и слитков.</p>
