        <h1>Припой оловянно-свинцовый ПОС</h1>
        <p>Припой – это материал или сплав, применяемый для соединения деталей пайкой. Температура плавления припоя, должна быть ниже паяемых материалов. Припой делится на твердый (температура плавления выше 400°С) и мягкий (температура плавления ниже 400°С). Основа большинства мягких припоев олово и свинец. </p>
        <p>Припой оловянно-свинцовый (ПОС) сплав с низкой температурой плавления. Припой ПОС характеризуется высокой пластичностью и хорошей смачиваемостью металлических поверхностей. Буквы ПОС - припой оловянно-свинцовый, цифры - содержание олова в процентах (ПОС 61, ПОС 40). Для получения особых свойств в состав оловянно-свинцовых припоев добавляют сурьму (Су), кадмий (К), висмут и другие металлы. Пример маркировки: припой ПОСК 50-18 содержит, олова - 50%, кадмия - 18% ; припой ПОССу 40-2 содержит олово - 40%, сурьмы - 2%. Сурьма – упрочняющая добавка однако она ухудшает смачивающую способность оловянно-свинцовых припоев, при содержании свыше 1% припой ПОС снижает пластичность и ударную вязкость. Пластичность и вязкость оловянно-свинцовых припоев заметно снижается при низких температурах.</p>
        <p>В зависимости от химического состава оловянно-свинцовые припои (ПОС) изготавливаются следующих марок: </p>
        <p><strong> бессурьмянистые</strong> - ПОС 90, ПОС 63, ПОС 61, ПОС 40, ПОС 30, ПОС 10, ПОС 61М, ПОСК 50-18, ПОСК 2-18; </p>
        <p><strong> малосурьмянистые</strong> - ПОССу 61-0,5, ПОССу 50-0,5, ПОССу 40-0,5, ПОССу 35-0,5, ПОССу 30-0,5, ПОССу 25-0,5, ПОССу 18-0,5;  </p>
        <p><strong> сурьмянистые</strong> - ПОСу 95-5, ПОССу 40-2, ПОССу 35-2, ПОССу 30-2, ПОССу 25-2, ПОССу 18-2, ПОССу 15-2, ПОССу 10-2, ПОССу 8-3; ПОССу 5-1, ПОССу 4-6, ПОССу 4-4.</p>

