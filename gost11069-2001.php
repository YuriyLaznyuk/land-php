        <h1>3 Классификация и состав</h1>
        <p>3.1 В зависимости от химического состава первичный алюминий подразделяется на алюминий высокой и технической чистоты.</p>
        <p>Химический состав марок алюминия, гарантированный изготовителем, приведен в таблице 1.</p>
        <p>В алюминии технической чистоты всех марок, используемом для изготовления пищевой посуды, массовая доля мышьяка должна быть не более 0,015 %.</p>
        <p>В алюминии высокой чистоты массовая доля магния не учитывается в сумме примесей при расчете <span class="alink">марки алюминия</span>.</p>
        <p>По согласованию с потребителем производитель определяет массовую долю натрия и лития и указывает результат с точностью до третьего знака после запятой в документе о качестве на конкретный вид продукции.</p>
