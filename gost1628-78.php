            <p>При изгибе образца его необточенная поверхность должна быть наружной.</p>
            <p>4.8. Химический состав определяют по ГОСТ 6689.23—92, ГОСТ 25086—87, ГОСТ 15027.1-77‚&nbsp; ГОСТ 15027.14-77, ГОСТ 20068.1-79—ГОСТ 20068.3-79 или другими методами, не уступающими по точности указанным в стандартах.</p>
