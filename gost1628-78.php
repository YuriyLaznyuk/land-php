            <p>3.6. Для проверки химического состава отбирают два прутка, два пучка или две бухты от партии. Допускается изготовителю проверку химического состава проводить на пробе, отобранной от расплавленного металла, при этом допускается контроль готовых прутков по химическому составу не проводить.</p>
            <p>3.7. При получении неудовлетворительных результатов испытания хотя бы по одному из показателей (кроме показателя внешнего вида) по нему проводят повторные испытания на удвоенной выборке, взятой от той же партии.</p>
            <p>Результаты повторного испытания распространяются на всю партию.</p>
            <p>Допускается изготовителю проводить поштучное испытание прутков.</p>
