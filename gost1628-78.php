            <p align="center"><b>4. МЕТОДЫ ИСПЫТАНИЙ</b></p>
            <p>4.1. Контроль внешнего вида прутков проводят с заданной вероятностью 96 % без применения увеличительных приборов.</p>
            <p>(Измененная редакция, Изм. № 4).</p>
            <p>4.2. Измерение размера прутка проводят не менее чем в двух взаимно перпендикулярных направлениях одного сечения прутка микрометром по ГОСТ 6507—90 ГОСТ 4381—87.</p>
            <p>Измерение длины проводят измерительной металлической рулеткой по ГОСТ 7502—80.</p>
            <p>Косину реза и кривизну прутков проверяют по ГОСТ 26877—86.</p>
            <p>Радиус закругления продольных ребер определяют радиусным шаблоном по ТУ 2—034—228—88.</p>
            <p>Допускается применять любые другие измерительные инструменты, если они обеспечивают заданную точность измерения.</p>
            <p>4.3. Проверку прутков на отсутствие внутренних дефектов производят неразрушающими методами контроля по методике, согласованной между изготовителем и потребителем.</p>
            <p>Допускается проверку прессованные прутков на наличие внутренних дефектов проводить неразрушающим методом контроля по методике, приведенной в справочном приложении 7 ГОСТ 2060—73 с конца, примыкающего к прессостатку.</p>
            <p>Для прессованных и катаных из прессованной заготовки прутков испытанию подвергают конец, примыкающий к прессостатку, а для тянутых прутков — оба конца.</p>
            <p>Допускается проводить проверку по излому прутка.</p>
            <p>Для получения излома пруток надрезают с одной или двух сторон, после чего ломают. Надрез должен быть сделан с таким расчетом, чтобы излом проходил через центральную часть прутка.</p>
            <p>Ширина излома должна быть: для прутков диаметром до 16 мм включ. не менее 60 % диаметра; для прутков диаметром свыше 16 мм — не менее 10 мм. Осмотр излома производят без применения увеличительных приборов.</p>
            <p>После осмотра допускается обломанный конец прутка не обрезать.</p>
            <p>(Измененная редакция, Изм. № 4).</p>
            <p>4.4. Испытание прутков на растяжение проводят на двух образцах, взятых от каждого отобранного от партии прутка или бухты. От каждого пучка, взятого от партии, отбирают по одному прутку и от каждого из прутков берут один образец. Испытание проводят по ГОСТ 1497—84.</p>
            <p>Для прутков из бронзы марки БрКМцЗ—1 испытание механических свойств проводят на образцах с расчетной длиной, равной пятикратному диаметру.</p>
            <p>Отбор образцов для испытания на растяжение производят в соответствии с ГОСТ 24047—80.</p>
            <p>4.5. Испытание прутков на твердость проводят на пяти образцах. При этом от отобранных от партии двух прутков или двух бухт берут пять образцов (два образца от одного прутка или бухты и три образца от другого прутка или бухты). От отобранных от партии двух пучков берут пять прутков (два прутка от одного пучка и три прутка от другого пучка) и от каждого из прутков берут один образец.</p>
            <p>Испытание на твердость по Бринеллю (НВ) проводят по ГОСТ 9012—59. Прутки диаметром 5—10 мм испытывают при условии НВ 2,5/187, 5/30, прутки диаметром 11—28 мм для интервала твердости 110—130 при условии НВ 5/250/30, для интервала твердости 130—220 при условии НВ 5/750/30, прутки диаметром более 28 мм для интервала твердости 110—130 при условии НВ 10/1000/30, для интервала 130—220 при условии НВ 10/3000/30.</p>
