            <p>(Измененная редакция, Изм. №4).</p>
            <p>4.7. Испытание прутков на изгиб проводят на двух образцах, отобранных от каждого прутка или бухты, взятых от партии. От каждого пучка отбирают по одному прутку и от каждого из прутков берут один образец. Испытание проводят по ГОСТ 14019—80.</p>
            <p>Изгиб проводят на 90° вокруг оправки с радиусом закругления, равным диаметру прутка, без появления следов надрыва и без отслоения.</p>
