            <p>б) мерной длины — в пределах немерной длины с предельными отклонениями;<br /> + 10 мм — для тянутых прутков всех размеров;<br /> + 15 мм — для прессованных и горячекатаных прутков размером до 50 мм;<br /> + 20 мм — для прессованных и горячекатаных прутков размером свыше 50 мм.</p>
            <p>Прессованные прутки мерной длины изготовляют по требованию потребителя;</p>
            <p>в) кратной мерной длины — в пределах немерной с припуском 5 мм на один рез и с общим допуском по длине в соответствии с подпунктом б.</p>
            <p>Предельные отклонения по длине мерных прессованных прутков повышенной точности диаметром до 80 мм должны быть + 10 мм, диаметром свыше 80 мм — +15 мм.</p>
            <p>По требованию потребителя тянутые прутки должны изготовлять с предельным отклонением по длине +6 мм.</p>
            <p>(Измененная редакция, Изм. № 2, 3).</p>
