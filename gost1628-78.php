            <p>3.4. Для проверки на скручивание от партии отбирают пять прутков. Если в партии менее пяти прутков, то контролируют каждый пруток.</p>
            <p>3.5. Отсутствие внутренних дефектов в прессованных и катаных из прессованной заготовки прутках проверяют на каждом прутке.</p>
            <p>Для проверки отсутствия внутренних дефектов&nbsp;&nbsp; в тянутых прутках отбирают два прутка, два пучка или две бухты от партии.</p>
